//1. Write a program that declares three variables of different types (int, double, String) and assigns values to them. Print these values.

package julyFifteen;

public class DatatypesBasic {
    public static void main(String[] args) {
        int numberValue = 45;
        double decimalValue = 45.67;
        String textValue = "Hello World";
        System.out.println("Integer Value: " + numberValue);
        System.out.println("Double Value: " + decimalValue);
        System.out.println("String Value: " + textValue);
    }
}
